# Example 1: Comparing numbers
x = 5
y = 10
if x == y:
    print("x is equal to y")
else:
    print("x is not equal to y")

# Example 2: Comparing strings
name1 = "John"
name2 = "John"
if name1 == name2:
    print("name1 is equal to name2")
else:
    print("name1 is not equal to name2")

